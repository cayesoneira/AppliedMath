%    of this approach.
function pdemodel
[pde_fig,ax]=pdeinit;
pdetool('appl_cb',9);
pdetool('snapon','on');
set(ax,'DataAspectRatio',[1 1 1]);
set(ax,'PlotBoxAspectRatio',[2.0098135426889105 1 5.1344086021505371]);
set(ax,'XLim',[-0.085720062272323247 0.30572006227232318]);
set(ax,'YLimMode','auto');
set(ax,'XTickMode','auto');
set(ax,'YTickMode','auto');
setappdata(ax,'extraspacex','-0.1:0.05:0.1');
pdetool('gridon','on');

% Geometry description:
pderect([0.02 0.099999999999999992 0.040000000000000008 0],'R1');
pderect([0.02 0.10000000000000001 0.040000000000000001 0.10000000000000001],'R2');
pderect([0.02 0.10000000000000001 0.10000000000000001 0.14000000000000001],'R3');
set(findobj(get(pde_fig,'Children'),'Tag','PDEEval'),'String','R1+R2+R3')

% Boundary conditions:
pdetool('changemode',0)
pdetool('removeb',[4 ]);
pdetool('removeb',[3 ]);
pdesetbd(8,...
'neu',...
1,...
'0',...
'0')
pdesetbd(7,...
'neu',...
1,...
'0',...
'500000*x')
pdesetbd(6,...
'neu',...
1,...
'0',...
'0')
pdesetbd(5,...
'neu',...
1,...
'50*x',...
'50*x*25')
pdesetbd(4,...
'neu',...
1,...
'50*x',...
'50*x*25')
pdesetbd(3,...
'neu',...
1,...
'50*x',...
'50*x*25')
pdesetbd(2,...
'neu',...
1,...
'0',...
'0')
pdesetbd(1,...
'dir',...
1,...
'1',...
'100')

% Mesh generation:
setappdata(pde_fig,'Hgrad',1.3);
setappdata(pde_fig,'refinemethod','regular');
setappdata(pde_fig,'jiggle',char('on','mean',''));
setappdata(pde_fig,'MesherVersion','preR2013a');
pdetool('initmesh')
pdetool('refine')
pdetool('refine')

% PDE coefficients:
pdeseteq(2,...
'52*x',...
'0',...
'(20000*x)+(0).*(0.0)',...
'(7800*x).*(500)',...
'0:10:1000',...
'100',...
'0.0',...
'[0 100]')
setappdata(pde_fig,'currparam',...
['7800*x ';...
'500    ';...
'52*x   ';...
'20000*x';...
'0      ';...
'0.0    '])

% Solve parameters:
setappdata(pde_fig,'solveparam',...
char('0','4416','10','pdeadworst',...
'0.5','longest','0','1E-4','','fixed','Inf'))

% Plotflags and user data strings:
setappdata(pde_fig,'plotflags',[1 1 1 1 2 1 7 1 0 0 1 101 1 0 0 1 0 1]);
setappdata(pde_fig,'colstring','');
setappdata(pde_fig,'arrowstring','');
setappdata(pde_fig,'deformstring','');
setappdata(pde_fig,'heightstring','');

% Solve PDE:
pdetool('solve')
